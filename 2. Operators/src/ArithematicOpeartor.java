public class ArithematicOpeartor {
    public static void main(String[] args) {
        // Arithmetic operators in Java are used to perform common
        // mathematical operations such as addition, subtraction,
        // multiplication, division, and modulus. These operators work
        // with all primitive data types such as int, float, double,
        // byte, short, and long.

        int a = 10;
        int b = 20;
        int sum = a + b;
        System.out.println("Sum: " + sum);
        int difference = a - b;
        System.out.println("Difference: " + difference);
        int product = a * b;
        System.out.println("Product: " + product);
        int quotient = a / b;
        System.out.println("Quotient: " + quotient);
        int remainder = a % b;
        System.out.println("Remainder: " + remainder);

        double c = 20;
        double d = 3;
        double sum_double = c + d;
        System.out.println("Sum: " + sum_double);
        double difference_double = c - d;
        System.out.println("Difference: " + difference_double);
        double product_double = c * d;
        System.out.println("Product: " + product_double);
        double quotient_double = c / d;
        System.out.println("Quotient: " + quotient_double);
        double remainder_double = c % d;
        System.out.println("Remainder: " + remainder_double);
    }
}
